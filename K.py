"""
В Трэшландии своеобразная финансовая система. Из математических операций используются
не только стандартные: +, -, *, / Но также квадратный корень и логарифм. Так что порой
бывает сложно посчитать сдачу в магазине.
Феликс и Бенедикт, юные жители Удотинска, пошли в магазин купить капустное мороженое.
Но у кассира бобры сгрызли счеты. Помогите ребятам узнать, сколько им должны дать сдачи.
Напишите функцию, вычисляющую квадратный корень из двух.
Формат вывода
Выведите значение квадратного корня из двух с точностью 5 знаков после запятой
Примечания
Функцией, вычисляющей корень, из стандартной библиотеки пользоваться нельзя.
"""

def babylonian_sqrt_recursive(x, guess=None, epsilon=1e-5):
    if guess is None:
        guess = x / 2  # Начальное приближение

    # Вычисляем новое приближение
    new_guess = (guess + x / guess) / 2

    # Проверяем, достигли ли мы нужной точности
    if abs(new_guess - guess) < epsilon:
        return new_guess

    # Рекурсивный вызов с новым приближением
    return babylonian_sqrt_recursive(x, new_guess, epsilon)

# Вычисляем корень из 2
result = babylonian_sqrt_recursive(2)
print(f"Корень из 2: {result:.5f}")